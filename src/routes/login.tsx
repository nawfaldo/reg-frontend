import { createFileRoute, useNavigate, Link } from "@tanstack/react-router";
import { useState } from "react";
import { authClient } from "../lib/auth-client";

export const Route = createFileRoute("/login")({
  component: Login,
});

function Login() {
  const navigate = useNavigate();

  const [email, setEmail] = useState("");
  const [password, setPassword] = useState("");
  const [isLoading, setIsLoading] = useState(false);

  const hour = new Date().getHours();
  let greeting = "Selamat pagi";
  if (hour >= 11 && hour < 15) {
    greeting = "Selamat siang";
  } else if (hour >= 15 && hour < 18) {
    greeting = "Selamat sore";
  } else if (hour >= 18 || hour < 4) {
    greeting = "Selamat malam";
  }

  const handleEmailLogin = async () => {
    setIsLoading(true);
    await authClient.signIn.email(
      { email, password },
      {
        onSuccess: () => {
          setIsLoading(false);
          navigate({ to: "/" });
        },
        onError: (ctx) => {
          alert(ctx.error.message);
          setIsLoading(false);
        },
      },
    );
  };

  const handleGoogleLogin = async () => {
    setIsLoading(true);
    await authClient.signIn.social({
      provider: "google",
      callbackURL: "https://reg-frontend-seven.vercel.app/client/dashboard",
    });
  };

  const inputClasses =
    "w-full p-2 bg-white border border-gray-300 rounded-lg outline-none focus:border-black transition-colors placeholder:text-gray-400 text-black text-sm";

  const buttonClasses =
    "w-full bg-black text-white p-2 rounded-lg font-bold text-sm hover:bg-gray-800 active:scale-[0.99] transition-all disabled:opacity-70";

  return (
    <div className="min-h-screen flex bg-white font-sans">
      <div className="w-full lg:w-1/2 flex flex-col justify-center p-4 lg:p-8">
        <div className="w-full max-w-xs mx-auto space-y-4">
          <div className="space-y-1">
            <h3 className="font-bold text-base mb-3 text-black">Netherium.</h3>
            <h1 className="text-2xl font-extrabold tracking-tight text-black">
              {greeting}
            </h1>
            <p className="text-gray-600 text-xs">
              Mohon sign in terlebih dahulu untuk mengakses layanan.
            </p>
          </div>

          <div className="space-y-3">
            <input
              type="email"
              placeholder="email"
              value={email}
              onChange={(e) => setEmail(e.target.value)}
              className={inputClasses}
              disabled={isLoading}
            />
            <input
              type="password"
              placeholder="password"
              value={password}
              onChange={(e) => setPassword(e.target.value)}
              className={inputClasses}
              disabled={isLoading}
            />

            <div className="flex items-center justify-between text-xs text-gray-600">
              <label className="flex items-center space-x-2 cursor-pointer">
                <input
                  type="checkbox"
                  className="accent-black w-3 h-3 rounded border-gray-300"
                />
                <span>Remember me</span>
              </label>
              <Link
                to="/forgot-password"
                className="hover:text-black hover:underline font-medium"
              >
                Forgot Password?
              </Link>
            </div>
          </div>

          <div className="space-y-3 pt-1">
            <button
              onClick={handleEmailLogin}
              disabled={isLoading}
              className={buttonClasses}
            >
              {isLoading ? "Signing In..." : "Sign In"}
            </button>

            <div className="relative flex py-1 items-center">
              <div className="flex-grow border-t border-gray-200"></div>
              <span className="flex-shrink mx-2 text-gray-400 text-[10px] font-medium">
                OR
              </span>
              <div className="flex-grow border-t border-gray-200"></div>
            </div>

            <button
              onClick={handleGoogleLogin}
              disabled={isLoading}
              className="w-full flex items-center justify-center gap-2 bg-white text-black p-2 rounded-lg font-bold text-sm border border-black hover:bg-gray-50 active:scale-[0.99] transition-all disabled:opacity-70"
            >
              <svg className="w-4 h-4" viewBox="0 0 24 24">
                <path
                  d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"
                  fill="#4285F4"
                />
                <path
                  d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"
                  fill="#34A853"
                />
                <path
                  d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"
                  fill="#FBBC05"
                />
                <path
                  d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"
                  fill="#EA4335"
                />
              </svg>
              <span>Sign in with Google</span>
            </button>
          </div>

          <p className="text-center text-gray-600 mt-4 text-xs">
            Don't have an account?{" "}
            <Link to="/signup" className="text-black font-bold hover:underline">
              Sign Up
            </Link>
          </p>
        </div>
      </div>

      <div className="hidden lg:flex w-1/2 bg-gray-100 items-center justify-center p-8"></div>
    </div>
  );
}
